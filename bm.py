def lfsr(m, state, cp, n):
	list_output = []
	for i in range(n):
		feedback = 0
		for s, c, in zip(state, cp):
			feedback += s*c
		feedback = feedback % 2
		list_output.append( (state[0] + m[i]) % 2 )
		state.pop(0)
		state.append(feedback)
	return list_output

def rev_lfsr(m, state, cp, n):
	list_output = []
	l = len(cp)
	for i in range(n):
		feedback = 0
		for s, c, in zip(state, cp):
			feedback += s*c
		feedback = feedback % 2
		list_output.append( (state[0] + m[i]) % 2 )
		state.pop(0)
		state.insert(0,feedback)
		state.insert(0, state[l-1])
		state.pop(l)
	return list_output

def berlekamp_massey_algorithm(seq):
	n = len(seq)
	c = numpy.zeros(n, dtype=numpy.int8)
	b = numpy.zeros(n, dtype=numpy.int8)
	c[0], b[0] = 1, 1
	l, m, i = 0, -1, 0
	intseq = seq
	
	while i < n:
		v = intseq[i-l : i]
		v = v[::-1]
		cc = c[1: l + 1]
		d = (intseq[i] + numpy.dot(v, cc)) % 2
		if d == 1:
			temp = c.copy()
			p = numpy.zeros(n, dtype=numpy.int8)
			for j in range(0, l):
				if b[j] == 1:
					p[j + i - m] = 1
			c = (c + p) % 2
			if l <= 0.5 * i:
				l = i + 1 - l
				m = i
				b = temp
		i += 1
	return l,c
	
def to_string(binaryvect):
	res = "'"
	n = int(len(binaryvect)/8)
	for i in range(n):
		split = binaryvect[8*i: 8*(i+1)]
		value = sum([ split[i]*2**(7-i) for i in range(8)])
		res=res+(chr(value))
	return res+"'"

